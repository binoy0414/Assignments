package language.basics;

public class BookTitleDataType {
    public static void main(String[] args) {
        // Data Type: String (for a sequence of characters/words)
        String bookTitle = "To Kill a Mockingbird";

        System.out.println("Value: " + bookTitle);
        System.out.println("Data Type: " + bookTitle.getClass().getSimpleName());
    }
}
